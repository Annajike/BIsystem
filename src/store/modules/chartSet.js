const actions = {
  getChart: function ({ commit }, params) {
    let obj = JSON.parse(JSON.stringify(chartSet[params.type]))
    if (params.legend) {
      for (let attr in params.legend) {
        obj.legend[attr] = params.legend[attr]
      }
    }
    if (params.title) {
      for (let attr in params.title) {
        obj.title[attr] = params.title[attr]
      }
    }
    commit('SET_CHART', obj)
  }
}

